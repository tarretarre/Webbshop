package Webbshop.Entiteter;

public class Kategori {

    protected int id;
    protected String namn;

    public Kategori(){}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNamn() {
        return namn;
    }

    public void setNamn(String namn) {
        this.namn = namn;
    }
}
